/* ===================== Small helpers ===================== */

static inline uint8_t xtime(uint8_t x) {
    return (uint8_t)((x << 1) ^ ((x & 0x80) ? 0x1B : 0x00));
}
static inline uint8_t mul(uint8_t x, uint8_t by) {
    /* multiply in GF(2^8) using Russian peasant method */
    uint8_t res = 0;
    while (by) {
        if (by & 1) res ^= x;
        x = xtime(x);
        by >>= 1;
    }
    return res;
}

/* ===================== Public helpers ===================== */

uint32_t rot_word(uint32_t w) {
    return (w << 8) | (w >> 24);
}
uint32_t sub_word(uint32_t w) {
    return ((uint32_t)sbox[(w >> 24) & 0xFF] << 24) |
           ((uint32_t)sbox[(w >> 16) & 0xFF] << 16) |
           ((uint32_t)sbox[(w >>  8) & 0xFF] <<  8) |
           ((uint32_t)sbox[(w      ) & 0xFF]);
}
void xor_bytes(uint8_t *dst, const uint8_t *a, const uint8_t *b, size_t len) {
    for (size_t i = 0; i < len; ++i) dst[i] = a[i] ^ b[i];
}

/* ===================== State conversions ===================== */

void state_from_bytes(state_t s, const uint8_t in[16]) {
    /* AES uses column-major: s[row][col] = in[row + 4*col] */
    for (int c = 0; c < 4; ++c)
        for (int r = 0; r < 4; ++r)
            s[r][c] = in[r + 4*c];
}
void bytes_from_state(uint8_t out[16], const state_t s) {
    for (int c = 0; c < 4; ++c)
        for (int r = 0; r < 4; ++r)
            out[r + 4*c] = s[r][c];
}

/* ===================== Round transforms ===================== */

void add_round_key(state_t s, const uint8_t *rk) {
    for (int c = 0; c < 4; ++c)
        for (int r = 0; r < 4; ++r)
            s[r][c] ^= rk[4*c + r];
}

void sub_bytes(state_t s) {
    for (int r = 0; r < 4; ++r)
        for (int c = 0; c < 4; ++c)
            s[r][c] = sbox[s[r][c]];
}
void inv_sub_bytes(state_t s) {
    for (int r = 0; r < 4; ++r)
        for (int c = 0; c < 4; ++c)
            s[r][c] = inv_sbox[s[r][c]];
}

void shift_rows(state_t s) {
    uint8_t t;

    /* Row 1: shift left by 1 */
    t = s[1][0]; s[1][0] = s[1][1]; s[1][1] = s[1][2]; s[1][2] = s[1][3]; s[1][3] = t;

    /* Row 2: shift left by 2 */
    t = s[2][0]; s[2][0] = s[2][2]; s[2][2] = t;
    t = s[2][1]; s[2][1] = s[2][3]; s[2][3] = t;

    /* Row 3: shift left by 3 (right by 1) */
    t = s[3][3]; s[3][3] = s[3][2]; s[3][2] = s[3][1]; s[3][1] = s[3][0]; s[3][0] = t;
}
void inv_shift_rows(state_t s) {
    uint8_t t;

    /* Row 1: shift right by 1 */
    t = s[1][3]; s[1][3] = s[1][2]; s[1][2] = s[1][1]; s[1][1] = s[1][0]; s[1][0] = t;

    /* Row 2: shift right by 2 (same as left by 2) */
    t = s[2][0]; s[2][0] = s[2][2]; s[2][2] = t;
    t = s[2][1]; s[2][1] = s[2][3]; s[2][3] = t;

    /* Row 3: shift right by 3 (left by 1) */
    t = s[3][0]; s[3][0] = s[3][1]; s[3][1] = s[3][2]; s[3][2] = s[3][3]; s[3][3] = t;
}

void mix_columns(state_t s) {
    for (int c = 0; c < 4; ++c) {
        uint8_t a0 = s[0][c], a1 = s[1][c], a2 = s[2][c], a3 = s[3][c];
        s[0][c] = (uint8_t)(mul(a0,2) ^ mul(a1,3) ^ a2 ^ a3);
        s[1][c] = (uint8_t)(a0 ^ mul(a1,2) ^ mul(a2,3) ^ a3);
        s[2][c] = (uint8_t)(a0 ^ a1 ^ mul(a2,2) ^ mul(a3,3));
        s[3][c] = (uint8_t)(mul(a0,3) ^ a1 ^ a2 ^ mul(a3,2));
    }
}
void inv_mix_columns(state_t s) {
    for (int c = 0; c < 4; ++c) {
        uint8_t a0 = s[0][c], a1 = s[1][c], a2 = s[2][c], a3 = s[3][c];
        s[0][c] = (uint8_t)(mul(a0,14) ^ mul(a1,11) ^ mul(a2,13) ^ mul(a3, 9));
        s[1][c] = (uint8_t)(mul(a0, 9) ^ mul(a1,14) ^ mul(a2,11) ^ mul(a3,13));
        s[2][c] = (uint8_t)(mul(a0,13) ^ mul(a1, 9) ^ mul(a2,14) ^ mul(a3,11));
        s[3][c] = (uint8_t)(mul(a0,11) ^ mul(a1,13) ^ mul(a2, 9) ^ mul(a3,14));
    }
}

/* ===================== Key expansion (AES-128) ===================== */

void key_expansion_128(const uint8_t key[16], uint8_t roundKeys[AES128_ROUND_KEYS_SIZE]) {
    /* roundKeys holds 11 round keys of 16 bytes each */
    memcpy(roundKeys, key, 16);
    uint32_t *w = (uint32_t*)roundKeys; /* 44 words (4 bytes each) */
    for (int i = 4; i < 44; ++i) {
        uint32_t temp = w[i-1];
        if (i % 4 == 0) {
            temp = sub_word(rot_word(temp)) ^ ((uint32_t)Rcon[i/4] << 24);
        }
        w[i] = w[i-4] ^ temp;
    }
}

/* ===================== Block cipher ===================== */

void aes_encrypt_block_128(uint8_t out[16], const uint8_t in[16], const uint8_t roundKeys[AES128_ROUND_KEYS_SIZE]) {
    state_t s;
    state_from_bytes(s, in);

    add_round_key(s, roundKeys +  0);
    for (int round = 1; round <= 9; ++round) {
        sub_bytes(s);
        shift_rows(s);
        mix_columns(s);
        add_round_key(s, roundKeys + 16*round);
    }
    sub_bytes(s);
    shift_rows(s);
    add_round_key(s, roundKeys + 160);

    bytes_from_state(out, s);
}

void aes_decrypt_block_128(uint8_t out[16], const uint8_t in[16], const uint8_t roundKeys[AES128_ROUND_KEYS_SIZE]) {
    state_t s;
    state_from_bytes(s, in);

    add_round_key(s, roundKeys + 160);
    for (int round = 9; round >= 1; --round) {
        inv_shift_rows(s);
        inv_sub_bytes(s);
        add_round_key(s, roundKeys + 16*round);
        inv_mix_columns(s);
    }
    inv_shift_rows(s);
    inv_sub_bytes(s);
    add_round_key(s, roundKeys + 0);

    bytes_from_state(out, s);
}

/* ===================== PKCS#7 ===================== */

int pkcs7_pad(const uint8_t *in, size_t in_len, uint8_t **out, size_t *out_len) {
    size_t pad = AES_BLOCK_SIZE - (in_len % AES_BLOCK_SIZE);
    if (pad == 0) pad = AES_BLOCK_SIZE;
    *out_len = in_len + pad;
    *out = (uint8_t*)malloc(*out_len);
    if (!*out) return -1;
    memcpy(*out, in, in_len);
    memset(*out + in_len, (int)pad, pad);
    return 0;
}

int pkcs7_unpad(uint8_t *buf, size_t *len) {
    if (*len == 0 || (*len % AES_BLOCK_SIZE) != 0) return -1;
    uint8_t pad = buf[*len - 1];
    if (pad == 0 || pad > AES_BLOCK_SIZE) return -1;
    for (size_t i = 0; i < pad; ++i) {
        if (buf[*len - 1 - i] != pad) return -1;
    }
    *len -= pad;
    return 0;
}

/* ===================== CBC mode ===================== */

int aes128_cbc_encrypt(const uint8_t *pt, size_t pt_len,
                       const uint8_t key[16], const uint8_t iv[16],
                       uint8_t **ct, size_t *ct_len)
{
    uint8_t *padded = NULL;
    size_t padded_len = 0;
    if (pkcs7_pad(pt, pt_len, &padded, &padded_len) != 0) return -1;

    *ct = (uint8_t*)malloc(padded_len);
    if (!*ct) { free(padded); return -1; }
    *ct_len = padded_len;

    uint8_t rk[AES128_ROUND_KEYS_SIZE];
    key_expansion_128(key, rk);

    uint8_t prev[16];
    memcpy(prev, iv, 16);

    for (size_t off = 0; off < padded_len; off += 16) {
        uint8_t block[16];
        xor_bytes(block, padded + off, prev, 16);
        aes_encrypt_block_128(*ct + off, block, rk);
        memcpy(prev, *ct + off, 16);
    }
    free(padded);
    return 0;
}

int aes128_cbc_decrypt(const uint8_t *ct, size_t ct_len,
                       const uint8_t key[16], const uint8_t iv[16],
                       uint8_t **pt, size_t *pt_len)
{
    if (ct_len == 0 || (ct_len % 16) != 0) return -1;

    *pt = (uint8_t*)malloc(ct_len);
    if (!*pt) return -1;
    *pt_len = ct_len;

    uint8_t rk[AES128_ROUND_KEYS_SIZE];
    key_expansion_128(key, rk);

    uint8_t prev[16];
    memcpy(prev, iv, 16);

    for (size_t off = 0; off < ct_len; off += 16) {
        uint8_t dec[16];
        aes_decrypt_block_128(dec, ct + off, rk);
        xor_bytes(*pt + off, dec, prev, 16);
        memcpy(prev, ct + off, 16);
    }

    if (pkcs7_unpad(*pt, pt_len) != 0) {
        free(*pt);
        *pt = NULL;
        *pt_len = 0;
        return -1;
    }
    return 0;
}
